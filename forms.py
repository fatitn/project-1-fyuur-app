import re

def isValidPhone(number):
    regex = re.compile('^([0-9]{3})-?([0-9]{3})-?([0-9]{4})$')
    return regex.match(number)

def isValidFacebook(url):

    regex = re.compile('^(https?:\\/\\/)?(w{3}\\.facebook.com\\/.*)')
    return regex.match(url)

 

